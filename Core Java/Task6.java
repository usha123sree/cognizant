package cognizant;

public class Task6 {

	    public static void main(String[] args) {
	        System.out.println("--- Primitive Data Type Demonstration ---");

	        int myInt = 42;
	        System.out.println("Integer (int): " + myInt);

	        float myFloat = 3.14f;
	        System.out.println("Float (float): " + myFloat);

	        double myDouble = 123.456789;
	        System.out.println("Double (double): " + myDouble);

	        char myChar = 'A';
	        System.out.println("Character (char): " + myChar);

	        boolean myBoolean = true;
	        System.out.println("Boolean (boolean): " + myBoolean);

	        boolean isJavaFun = true;
	        boolean isRaining = false;
	        System.out.println("Boolean (isJavaFun): " + isJavaFun);
	        System.out.println("Boolean (isRaining): " + isRaining);

	        long myLong = 123456789012345L;
	        System.out.println("Long (long): " + myLong);

	        byte myByte = 120;
	        System.out.println("Byte (byte): " + myByte);

	        short myShort = 30000;
	        System.out.println("Short (short): " + myShort);
	    }
	}

